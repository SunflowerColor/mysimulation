z=[];
p=[-2 -8 -0.5-0.866i -0.5+0.866i];
k=[1.05/2/8];
z1=[];
p1=[-2 -8 -0.5-0.866i -0.5+0.866i];
k1=[1.05/2/8];
sys1=zpk(z,p,k);
