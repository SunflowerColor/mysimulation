clc;
clear all;
close all;
s=tf('s');
a1=-1;
b1=2;
sysfs1=s*s-2*a1*s+a1*a1+b1*b1;
sys1=1/(s+2)/(sysfs1)/(1/8*s+1);
sys2=1/(s+2)/(sysfs1);
t=0:0.01:10;
y1=step(sys1,t);
y2=step(sys2,t);
G2=zpk(sys2);
[z2,p2,k]=zpkdata(G2,'v');
z2=z2';
p2=p2';
zs2=num2str(z2);
ps2=num2str(p2);
text=['零点为',zs2,'  ','极点为',ps2];
[v1,v2,v3,v4,v5,v6,v71]=value(y1,t(2)-t(1));
[v1,v2,v3,v4,v5,v6,v72]=value(y2,t(2)-t(1));
plot(t,y1,'r--',t,y2,'k');
legend(v71,v72,4);
title (text);